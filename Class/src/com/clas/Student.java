package com.clas;

public class Student {
	
	    int id;
	    String name;
	    String adress;
	    int zip;
	    public int getId() {
	        return id;
	    }
	    public void setId(int id) {
	        this.id = id;
	    }
	    public String getName() {
	        return name;
	    }
	    public void setName(String name) {
	        this.name = name;
	    }
	    public String getAdress() {
	        return adress;
	    }
	    public void setAdress(String adress) {
	        this.adress = adress;
	    }
	    public int getZip() {
	        return zip;
	    }
	    public void setZip(int zip) {
	        this.zip = zip;
	    }



	}
	
	 
	


